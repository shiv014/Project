package madeClasses;
import java.util.*;

public class print {

		public print(int[] arr) {
			for(int n:arr) {
			System.out.print(n+" ");
		}
		}
		
		public print(String[] arr) {
			for(String n:arr) {
			System.out.print(n+" ");
		}
		}
			
		public print(ArrayList<Integer> arr) {
			for(Integer n:arr) {
				System.out.print(n+" ");
		}
		}
}
